public class Array_practice5_set {
    public static void main(String[] args) {
     //java program toreverse the array...
        int[] marks={90,89,98,70,67,45,78};
        System.out.println("Reversing the order of the array elements:");
        for(int i=marks.length-1;i>=0;i--){
            System.out.println(marks[i]);
        }
    }
}
