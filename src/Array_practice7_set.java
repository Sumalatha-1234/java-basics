public class Array_practice7_set {
    public static void main(String[] args) {
        //java program to find the minimum element in an array...
        int[] min={92,67,35,45,120,105,100,98,78,65};
        System.out.println("The minimum number in the given array is:");
        int arr1=min[2];
        for(int i=0;i<=min.length;i++){
            if(arr1==min[2]){
                System.out.println(min[2]);
                break;
            }
        }
    }
}
