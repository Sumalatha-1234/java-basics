public class datatypes {
    public static void main(String[] args) {
        int num1=10;
        float num2=5.678f;
        double num3=3.4567d;
        char str='A';
        boolean bool=true;
        System.out.println("The data type is of integer type");
        System.out.println(num1);
        System.out.println("The data type is of float type");
        System.out.println(num2);
        System.out.println("The data type is of double type");
        System.out.println(num3);
        System.out.println("The data type is of String type");
        System.out.println(str);
        System.out.println("The data type is of boolean type");
        System.out.println(bool);
    }
}
