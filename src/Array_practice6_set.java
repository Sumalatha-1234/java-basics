public class Array_practice6_set {
    public static void main(String[] args) {
        //java program to find the maximum element in an array...
        int[] max={92,67,35,45,120,105,100,98,78,65};
        System.out.println("The maximum number in the given array is:");
        int arr=max[4];
        for(int i=0;i<=max.length;i++){
            if(arr==max[4]){
                System.out.println(max[4]);
                break;
            }
        }
    }
}
